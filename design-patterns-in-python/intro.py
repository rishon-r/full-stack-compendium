# WHAT IS A DESIGN PATTERN?

# Over the course of software engineering, software engineers have encountered certain problems that occur commonly
# Due to this, certain standard solutions to those problems have been accepted as a sort of convention in the industry
# This is due to the fact that those solutions are the most optimized for scalability, maintainability, and reusability
# These standard solutions have been termed as DESIGN PATTERNS

# Design patterns are generally split into three categories:
# 1. CREATIONAL - concerned with how objects are created e.g Singleton, Factory
# 2. STRUCTURAL - concerned with how classes and objects are composed e.g Decorator
# 3. BEHAVIOURAL - concerned with how objects communicate with each other e.g Observer